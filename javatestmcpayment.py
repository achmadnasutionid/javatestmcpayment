def numberOneBigO2(array):
  result = 0
  for i in range(0, len(array)):
    for j in range(0, len(array)):
      if (array[i]-array[j]) < 0:
        break
      else:
        result = array[i]
  return result

# Solusi yang sudah dioptimasi
def numberOneBigO(array):
  max = 0
  for element in array:
    if element > max:
      max = element
  return max

array = [3, 1, 4, 2]
array2 = [1, 2, 3, 4]

print(numberOneBigO2(array))
print(numberOneBigO2(array2))

print(numberOneBigO(array))
print(numberOneBigO(array2))

