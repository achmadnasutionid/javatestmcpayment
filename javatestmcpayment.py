def numberTwo(array, x):
  result = []
  for i in range(0, len(array)):
    full_loop = False
    for j in range(0, len(array)):
      if (array[i]/array[j] == x):
        full_loop = False
        break
      else:
        full_loop = True
    if full_loop:
      result.append(array[i])
  return result

array = [1, 2, 3, 4]
x = 4
x2 = 1

print(numberTwo(array, x))
print(numberTwo(array, x2))

